#test the function
add_two_numbers(5,6)

subtract_two_numbers <- function(num1, num2){num1 - num2}
#test the function
subtract_two_numbers(5,6)

multiply_two_numbers <- function(num1, num2){num1 * num2}
#test the function
multiply_two_numbers(5,6)

divide_two_numbers <- function(num1, num2){num1 / num2}
#test the function
divide_two_numbers(5,6)

exponential_two_numb <- function(numb1, numb2){numb1 ** numb2}
#test the function
exponential_two_numb(5,3)

cube <- function(numb1){numb1 * numb1 * numb1}
